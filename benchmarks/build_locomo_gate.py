#!/usr/bin/env python3
"""Build the frozen LoCoMo-100 fast CI gate from the full LoCoMo fixtures.

Retires the synthetic 108-case smoke corpus: instead of hand-crafted queries,
the per-PR recall-regression gate runs 100 REAL LoCoMo cases against a single
conversation's corpus (small enough to ingest in ~1-2 min, real enough to be
meaningful). Deterministic selection — same input always yields the same 100
cases — so the frozen baseline is stable.

Source (generated by locomo_to_harness.py):
  tests/recall/corpora/locomo.jsonl   — all 5,882 dialogue turns
  tests/recall/locomo_cases.jsonl     — all 1,531 QA cases

Output (the frozen gate fixtures):
  tests/recall/corpora/locomo-gate.jsonl  — conv-42's turns (the corpus)
  tests/recall/locomo-gate-cases.jsonl    — 100 category-balanced conv-42 cases

conv-42 chosen because it has the largest case pool spanning all four LoCoMo
categories. Cases are selected by sorted id (deterministic) up to a per-category
target, so the mix is balanced and reproducible.

Usage:
  python benchmarks/build_locomo_gate.py
"""
from __future__ import annotations

import json
import os

CONV = "conv-42"
# Category targets summing to 100; open_domain is scarce so take what exists.
TARGETS = {"single_hop": 35, "temporal": 30, "multi_hop": 25, "open_domain": 10}
CORPUS_IN = "tests/recall/corpora/locomo.jsonl"
CASES_IN = "tests/recall/locomo_cases.jsonl"
CORPUS_OUT = "tests/recall/corpora/locomo-gate.jsonl"
CASES_OUT = "tests/recall/locomo-gate-cases.jsonl"


def main() -> None:
    corpus = [json.loads(l) for l in open(CORPUS_IN, encoding="utf-8") if l.strip()]
    cases = [json.loads(l) for l in open(CASES_IN, encoding="utf-8") if l.strip()]

    conv_turns = [t for t in corpus if t["id"].split(":")[0] == CONV]
    conv_turn_ids = {t["id"] for t in conv_turns}

    # Keep only cases of this conversation whose gold is fully inside its corpus.
    conv_cases = [
        c
        for c in cases
        if c["id"].split("_q")[0] == CONV
        and all(r["corpus_item_id"] in conv_turn_ids for r in c.get("relevant", []))
    ]

    # Deterministic, category-balanced selection.
    by_cat: dict[str, list] = {}
    for c in sorted(conv_cases, key=lambda c: c["id"]):
        by_cat.setdefault(c["category"], []).append(c)

    selected: list = []
    for cat, target in TARGETS.items():
        selected.extend(by_cat.get(cat, [])[:target])
    # Top up to 100 from any remaining cases if a category was short.
    if len(selected) < 100:
        chosen = {c["id"] for c in selected}
        for c in sorted(conv_cases, key=lambda c: c["id"]):
            if c["id"] not in chosen:
                selected.append(c)
                if len(selected) >= 100:
                    break
    selected = selected[:100]
    selected.sort(key=lambda c: c["id"])

    os.makedirs(os.path.dirname(CORPUS_OUT), exist_ok=True)
    with open(CORPUS_OUT, "w", encoding="utf-8") as f:
        for t in conv_turns:
            f.write(json.dumps(t, ensure_ascii=False) + "\n")
    with open(CASES_OUT, "w", encoding="utf-8") as f:
        for c in selected:
            f.write(json.dumps(c, ensure_ascii=False) + "\n")

    import collections

    mix = collections.Counter(c["category"] for c in selected)
    print(
        f"LoCoMo-100 gate: corpus={len(conv_turns)} turns ({CONV}), "
        f"cases={len(selected)} {dict(mix)}"
    )


if __name__ == "__main__":
    main()
